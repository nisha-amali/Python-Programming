# Logical Operators
a = 5
b = 3
# and operator
print("a > 2 and b > 1: ",a > 2 and b > 1)
print("a > 2 and b > 5: ",a > 2 and b > 5)
# or operator
print("a > 2 or b > 5: ",a > 2 or b > 5)
print("a < 0 or b < 0: ",a < 1 or b < 0)
# not operator
print("not(a > 2):",not(a > 2))
print("not(a > 5):",not(a > 5))