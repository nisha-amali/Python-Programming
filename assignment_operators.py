# Equals
x = 10
print("x = ",x)
# +=
a = 10
a += 5  # a = a+5
print("After += 5 : ",a)
# -=
b = 20
b -= 7  # b = b-7
print("After -= 7 : ",b)
# *=
c = 6
c *= 4  # c= c*4
print("After *= 4 : ",c)
# /=
d = 50
d /= 8  # d= d/8
print("After /= 8 : ",d)
# %=
e = 29
e %= 5  # e = e%5
print("After %= 5 : ",e)
# **
f = 3
f **= 4  # f = f**4
print("After **= 4 : ",f)
# //
g = 50
g //= 7  # g = g//7
print("After //= 7 : ",g)